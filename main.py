from machine import Pin
import servo
import motor
from time import sleep

D1 = Pin(10, Pin.OUT)
D2 = Pin(11, Pin.OUT)
D3 = Pin(12, Pin.OUT)
D4 = Pin(26, Pin.OUT)
D5 = Pin(27, Pin.OUT)

while True:
  servo.angle(servo.SV1, 0)
  servo.angle(servo.SV2, 200)
  servo.angle(servo.SV3, 0)
  motor.move(1, 50)
  D1.value(1)
  D2.value(1)
  D3.value(1)
  D4.value(1)
  D5.value(1)
  sleep(0.5)
  servo.angle(servo.SV1, 90)
  servo.angle(servo.SV2, 90)
  servo.angle(servo.SV3, 90)
  motor.move(1, 100)
  D1.value(0)
  D2.value(0)
  D3.value(0)
  D4.value(0)
  D5.value(0)
  sleep(0.5)
  servo.angle(servo.SV1, 200)
  servo.angle(servo.SV2, 0)
  servo.angle(servo.SV3, 180)
  D1.value(1)
  D2.value(1)
  D3.value(0)
  D4.value(0)
  D5.value(0)
  motor.move(2, 50)
  sleep(0.5)
  D1.value(0)
  D2.value(0)
  D3.value(1)
  D4.value(1)
  D5.value(1)
  motor.move(2, 100)
  sleep(0.5)
